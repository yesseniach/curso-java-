# Division exacta (/)
print(50/35) #Division exacta 1.428571428571
print(round(50/35,2)) # 1.43 redondea

# Division entera (//)
print(50//35) #pierde la parte decimal

regalos = 123
alumnos = 25

regalos_por_persona = regalos // alumnos
print(f'Regalos por persona {regalos_por_persona}')
print(f'regalos por persona {regalos//alumnos}')


# Exponente (**)
print(2**3) #8
print(3**3) #27